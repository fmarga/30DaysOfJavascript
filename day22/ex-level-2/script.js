//setting variables
const title = document.createElement('h1');
const subtitle = document.createElement('h3');
const p = document.createElement('p');
const div = document.createElement('div');
let country;


//changing content and style
document.body.style.fontFamily = 'Arial';

title.textContent = 'WORLD COUNTRIES LIST';
title.style.fontSize = '30px';
title.style.textAlign = 'center';

subtitle.textContent = `Total number of countries: ${countries.length}`;
subtitle.style.fontSize = '18px';
subtitle.style.textAlign = 'center';

p.textContent = '30DaysOfJavaScript:DOM-Day-2';
p.style.fontSize = '14px';
p.style.textDecoration = 'underline';
p.style.textAlign = 'center';

div.style.width = '80%';
div.style.margin = 'auto';
div.style.display = 'grid';
div.style.gridTemplateColumns = '1fr 1fr 1fr 1fr 1fr 1fr';

